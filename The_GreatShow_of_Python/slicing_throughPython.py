# indexing 



name = "Bro Code"

# just want to have everything 

print(name)

full_name = name[0:] # first to end

first_name = name[:3] # first to third

funky_Name = name[::3] # skipping word

reversed_Word = name[::-1] # [0:end:-1]

# print the first letter 
first_word = name[0] # first word only
print(first_name)

print(full_name)

print(first_word)

print(funky_Name)

print(reversed_Word)

